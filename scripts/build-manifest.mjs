#!/usr/bin/env node
// Scans pieces/ for folders containing meta.json and writes manifest.js at the
// repo root. Node stdlib only — no dependencies, so this script keeps working
// long after npm packages rot. Folders starting with "_" or "." are skipped.

import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..');
const piecesDir = join(root, 'pieces');

const entries = await readdir(piecesDir, { withFileTypes: true });
const pieces = [];

for (const entry of entries) {
  if (!entry.isDirectory()) continue;
  if (entry.name.startsWith('_') || entry.name.startsWith('.')) continue;

  const metaPath = join(piecesDir, entry.name, 'meta.json');
  try {
    const meta = JSON.parse(await readFile(metaPath, 'utf8'));
    pieces.push({ slug: entry.name, ...meta });
  } catch (err) {
    console.warn(`Skipping ${entry.name}: ${err.message}`);
  }
}

pieces.sort((a, b) => a.slug.localeCompare(b.slug));

const out =
  '// Generated by scripts/build-manifest.mjs — do not edit by hand.\n' +
  'window.HYPERGAL_MANIFEST = ' +
  JSON.stringify({ pieces }, null, 2) +
  ';\n';

await writeFile(join(root, 'manifest.js'), out);
console.log(`Wrote manifest.js with ${pieces.length} piece(s).`);
